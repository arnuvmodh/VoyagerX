package com.example.meepmeeptesting;

public class SubmersibleDisplay {
    private double _x;
    private double _y;
    public SubmersibleDisplay() {
        // x = 21.375 in, y = 13.75 in
        _x = 22;
        _y = 6;
    }
    public SubmersibleDisplay(double x, double y) {
        _x = x;
        _y = y;
    }

}
